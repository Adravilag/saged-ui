import { Component, Prop, Event, EventEmitter, State, h, Watch } from '@stencil/core';

export type ThemeMode = 'light' | 'dark' | 'system';

@Component({
  tag: 'sg-theme-toggle',
  styleUrl: 'theme-toggle.css',
  shadow: true,
})
export class SgThemeToggle {
  /**
   * The current theme mode
   */
  @Prop({ mutable: true, reflect: true }) theme: ThemeMode = 'system';

  /**
   * Size of the toggle button
   */
  @Prop() size: 'sm' | 'md' | 'lg' = 'md';

  /**
   * Internal state for the resolved theme (light or dark)
   */
  @State() resolvedTheme: 'light' | 'dark' = 'light';

  /**
   * Emitted when the theme changes
   */
  @Event() sgThemeChange: EventEmitter<ThemeMode>;

  private mediaQuery: MediaQueryList | null = null;

  connectedCallback() {
    this.setupSystemThemeListener();
    this.updateResolvedTheme();
    this.applyThemeToDocument();
  }

  disconnectedCallback() {
    this.removeSystemThemeListener();
  }

  @Watch('theme')
  onThemeChange() {
    this.updateResolvedTheme();
    this.applyThemeToDocument();
  }

  private setupSystemThemeListener() {
    if (typeof window !== 'undefined' && window.matchMedia) {
      this.mediaQuery = window.matchMedia('(prefers-color-scheme: dark)');
      this.mediaQuery.addEventListener('change', this.handleSystemThemeChange);
    }
  }

  private removeSystemThemeListener() {
    if (this.mediaQuery) {
      this.mediaQuery.removeEventListener('change', this.handleSystemThemeChange);
    }
  }

  private handleSystemThemeChange = () => {
    if (this.theme === 'system') {
      this.updateResolvedTheme();
      this.applyThemeToDocument();
    }
  };

  private updateResolvedTheme() {
    if (this.theme === 'system') {
      this.resolvedTheme = this.getSystemTheme();
    } else {
      this.resolvedTheme = this.theme;
    }
  }

  private getSystemTheme(): 'light' | 'dark' {
    if (typeof window !== 'undefined' && window.matchMedia) {
      return window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light';
    }
    return 'light';
  }

  private applyThemeToDocument() {
    if (typeof document !== 'undefined') {
      document.documentElement.setAttribute('data-theme', this.resolvedTheme);
    }
  }

  private cycleTheme = () => {
    const modes: ThemeMode[] = ['light', 'dark', 'system'];
    const currentIndex = modes.indexOf(this.theme);
    const nextIndex = (currentIndex + 1) % modes.length;
    this.theme = modes[nextIndex];
    this.sgThemeChange.emit(this.theme);
  };

  private getSizeClass(): string {
    return `toggle--${this.size}`;
  }

  render() {
    return (
      <button
        type="button"
        class={`toggle ${this.getSizeClass()}`}
        onClick={this.cycleTheme}
        aria-label={`Current theme: ${this.theme}. Click to change.`}
        title={`Theme: ${this.theme}`}
      >
        <span class={`icon icon--sun ${this.resolvedTheme === 'light' ? 'icon--active' : ''}`}>
          <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
            <circle cx="12" cy="12" r="5" />
            <line x1="12" y1="1" x2="12" y2="3" />
            <line x1="12" y1="21" x2="12" y2="23" />
            <line x1="4.22" y1="4.22" x2="5.64" y2="5.64" />
            <line x1="18.36" y1="18.36" x2="19.78" y2="19.78" />
            <line x1="1" y1="12" x2="3" y2="12" />
            <line x1="21" y1="12" x2="23" y2="12" />
            <line x1="4.22" y1="19.78" x2="5.64" y2="18.36" />
            <line x1="18.36" y1="5.64" x2="19.78" y2="4.22" />
          </svg>
        </span>
        <span class={`icon icon--moon ${this.resolvedTheme === 'dark' ? 'icon--active' : ''}`}>
          <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
            <path d="M21 12.79A9 9 0 1 1 11.21 3 7 7 0 0 0 21 12.79z" />
          </svg>
        </span>
        {this.theme === 'system' && <span class="badge">auto</span>}
      </button>
    );
  }
}
